//! GTM Landing Page Engine — discriminant enums
//!
//! All string-backed fields on `platform_products`, `product_page_variants`,
//! `product_tracking_pixels`, and `pub_products` lead-capture are expressed
//! here as proper Rust enums so the compiler enforces exhaustive match and
//! serde rejects unknown variants at deserialization boundaries.
//!
//! Pattern follows the existing codebase conventions in `types/lead.rs` and
//! `types/account.rs`:
//!   - `#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]`
//!   - `#[serde(rename_all = "snake_case")]` on the enum level
//!   - `impl Display` mapping each variant to its DB string
//!   - `impl TryFrom<String>` returning `Err(String)` on unknown discriminants

use std::fmt;
use serde::{Deserialize, Serialize};

// ── LaunchMode ────────────────────────────────────────────────────────────────

/// Controls what a visitor sees when they land on a product page.
///
/// Stored as VARCHAR in `platform_products.launch_mode` and
/// `product_page_variants.launch_mode`.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum LaunchMode {
    /// Product is fully live — show CTA to sign up / subscribe.
    Active,
    /// Product is in pre-launch — collect email for waitlist.
    Waitlist,
    /// Product is available for pre-order with Stripe Checkout.
    PreOrder,
    /// Internal soft-launch phase — shown only to approved testers.
    PreLaunch,
    /// Not yet ready for any traffic. Renders 404 / coming-soon.
    Draft,
}

impl LaunchMode {
    /// Returns `true` if visitors can complete a conversion action on the page.
    pub fn allows_conversion(&self) -> bool {
        matches!(self, Self::Active | Self::PreOrder | Self::Waitlist)
    }

    /// Returns `true` if the page should be indexed by search engines.
    pub fn is_indexable(&self) -> bool {
        matches!(self, Self::Active | Self::Waitlist | Self::PreOrder)
    }
}

impl fmt::Display for LaunchMode {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.write_str(match self {
            Self::Active    => "active",
            Self::Waitlist  => "waitlist",
            Self::PreOrder  => "pre_order",
            Self::PreLaunch => "pre_launch",
            Self::Draft     => "draft",
        })
    }
}

impl TryFrom<String> for LaunchMode {
    type Error = String;
    fn try_from(s: String) -> Result<Self, Self::Error> {
        match s.as_str() {
            "active"     => Ok(Self::Active),
            "waitlist"   => Ok(Self::Waitlist),
            "pre_order"  => Ok(Self::PreOrder),
            "pre_launch" => Ok(Self::PreLaunch),
            "draft"      => Ok(Self::Draft),
            other        => Err(format!("unknown LaunchMode: '{other}'")),
        }
    }
}

impl TryFrom<&str> for LaunchMode {
    type Error = String;
    fn try_from(s: &str) -> Result<Self, Self::Error> {
        Self::try_from(s.to_string())
    }
}

