// ── AppId ──────────────────────────────────────────────────────────────────────

/// Identifies which Atlas app binary owns a given `platform_product`.
///
/// Values mirror the `&'static str` returned by each `AtlasApp::app_id()` impl.
/// Stored as `platform_products.app_slug` so the edge resolver can route
/// a domain request to the correct app SSR binary without slug-guessing.
///
/// # Keeping in sync
/// When a new `AtlasApp` impl is added in `atlas_apps/`, add its `app_id()` value
/// here AND update the DB CHECK constraint in the migration. The compiler will
/// flag every non-exhaustive match site automatically.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum AppId {
    /// `FolioApp` — property management (LTR, STR, wholesaling)
    PropertyManagement,
    /// `AnchorApp` — anchor pages / classified directory
    Anchor,
    /// `NetworkInstanceApp` — network instance (portals, B2B directories)
    NetworkInstance,
    /// `MeridianApp` — hospitality / direct booking engine
    Meridian,
    /// `CorePlatformApp` — platform infrastructure (used for meta-products)
    CorePlatform,
}

impl AppId {
    /// The canonical string stored in the DB and returned in API responses.
    ///
    /// These must match the corresponding `AtlasApp::app_id()` return values.
    pub fn as_str(&self) -> &'static str {
        match self {
            Self::PropertyManagement => "property_management",
            Self::Anchor             => "anchor",
            Self::NetworkInstance    => "network_instance",
            Self::Meridian           => "meridian",
            Self::CorePlatform       => "core_platform",
        }
    }

    /// All valid DB values — used to generate the CHECK constraint in migrations.
    pub fn all_db_values() -> &'static [&'static str] {
        &[
            "property_management",
            "anchor",
            "network_instance",
            "meridian",
            "core_platform",
        ]
    }

    /// The marketing-facing slug used in URLs and product records.
    /// e.g. `PropertyManagement → "folio"`.
    pub fn product_slug(&self) -> &'static str {
        match self {
            Self::PropertyManagement => "folio",
            Self::Anchor             => "anchor",
            Self::NetworkInstance    => "network_instance",
            Self::Meridian           => "meridian",
            Self::CorePlatform       => "core",
        }
    }
}

impl fmt::Display for AppId {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.write_str(self.as_str())
    }
}

impl TryFrom<String> for AppId {
    type Error = String;
    fn try_from(s: String) -> Result<Self, Self::Error> {
        match s.as_str() {
            "property_management" => Ok(Self::PropertyManagement),
            "anchor"              => Ok(Self::Anchor),
            "network_instance"    => Ok(Self::NetworkInstance),
            "meridian"            => Ok(Self::Meridian),
            "core_platform"       => Ok(Self::CorePlatform),
            other                 => Err(format!("unknown AppId: '{other}'")),
        }
    }
}

impl TryFrom<&str> for AppId {
    type Error = String;
    fn try_from(s: &str) -> Result<Self, Self::Error> {
        Self::try_from(s.to_string())
    }
}

