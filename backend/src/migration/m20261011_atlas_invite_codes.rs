///
///   "Join as a cohost for this portfolio"
///     → role=cohost, landlord_id=<owner>, max_uses=5
///
///   "You're an agent at this brokerage"
///     → role=agent, broker_id=<broker>, max_uses=unlimited
///
/// ## Resolution Flow
///   GET /join/{code}
///   → resolve code → role + context
///   → redirect to /folio/onboard/{role}?code={code}
///   → wizard left panel shows resolved entity name + message
///   → on completion, platform_invite is created with all resolved FKs
///   → uses_count incremented; deactivated when uses_count >= max_uses
///
/// ## Code Format
///   Generated by the platform or landlord:
///   - Human-readable prefix + random suffix: "OAK4B-K7X", "VILLA-2025", "WO-A93F"
///   - Platform-admin may use UUID-short: "inv_a3b9c7d1"
