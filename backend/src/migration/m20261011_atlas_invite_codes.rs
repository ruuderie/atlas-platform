/// Create `atlas_invite_codes` — short-token invite system for deep-link provisioning.
///
/// An invite code is a human-readable token that pre-resolves all the context
/// needed to provision a new user into a specific role + entity relationship.
///
/// ## Core Use Cases
///
///   "Apply for THIS apartment"
///     → role=tenant, asset_id=<unit>, landlord_id=<PM>, max_uses=unlimited
